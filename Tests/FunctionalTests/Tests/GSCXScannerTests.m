      performAction:grey_tapAtPoint(kGSCXTestOverlappingRingViewTapOffset)];
  [self gscxtest_assertLabelForCheckNamed:kGSCXTestCheckName1 isSufficientlyVisible:NO];
  [self gscxtest_assertLabelForCheckNamed:kGSCXTestCheckName2 isSufficientlyVisible:YES];
  [self gscxtest_assertLabelForCheckNamed:kGSCXTestCheckName3 isSufficientlyVisible:NO];
  [self gscxtest_assertLabelForCheckNamed:kGSCXTestCheckName4 isSufficientlyVisible:NO];
  [self gscxtest_tapBackButtonWithAccessibilityLabel:@"4 Issues"];
  [GSCXScannerTestUtils dismissScreenshotView];
}

