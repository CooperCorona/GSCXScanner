- (void)testTappingOnRingViewWithMultipleIssuesPresentsGalleryView {
  [GSCXScannerTestUtils openPage:[GSCXTestScannerViewController class]];
  [GSCXScannerTestUtils tapPerformScanButton];
  [[self gscxtest_selectRingViewAtIndex:0]
      assertWithMatcher:grey_accessibilityLabel(
                            @"2 issues for element with accessibility label Fail Checks 1 and 4")];
  [[self gscxtest_selectRingViewAtIndex:0] performAction:grey_tap()];
  [self gscxtest_assertLabelForCheckNamed:kGSCXTestCheckName1 isSufficientlyVisible:YES];
  [self gscxtest_assertLabelForCheckNamed:kGSCXTestCheckName2 isSufficientlyVisible:NO];
  [self gscxtest_assertLabelForCheckNamed:kGSCXTestCheckName3 isSufficientlyVisible:NO];
  [self gscxtest_assertLabelForCheckNamed:kGSCXTestCheckName4 isSufficientlyVisible:YES];
  [self gscxtest_tapBackButtonWithAccessibilityLabel:@"4 Issues"];
  [GSCXScannerTestUtils dismissScreenshotView];
}

- (void)testTappingOnRingViewWithSingleIssuePresentsGalleryView {
  [GSCXScannerTestUtils openPage:[GSCXTestScannerViewController class]];
  [GSCXScannerTestUtils tapPerformScanButton];
  [[self gscxtest_selectRingViewAtIndex:1]
      assertWithMatcher:grey_accessibilityLabel(
                            @"1 issue for element with accessibility label Fails Check 2")];
  [[self gscxtest_selectRingViewAtIndex:1] performAction:grey_tap()];
  [self gscxtest_assertLabelForCheckNamed:kGSCXTestCheckName1 isSufficientlyVisible:NO];
  [self gscxtest_assertLabelForCheckNamed:kGSCXTestCheckName2 isSufficientlyVisible:YES];
  [self gscxtest_assertLabelForCheckNamed:kGSCXTestCheckName3 isSufficientlyVisible:NO];
  [self gscxtest_assertLabelForCheckNamed:kGSCXTestCheckName4 isSufficientlyVisible:NO];
  [self gscxtest_tapBackButtonWithAccessibilityLabel:@"4 Issues"];
  [GSCXScannerTestUtils dismissScreenshotView];
}

- (void)testTappingOnOverlappingRingViewsEachWithSingleIssueGalleryViewForFirstIndex {
  [GSCXScannerTestUtils openPage:[GSCXTestScannerViewController class]];
  [GSCXScannerTestUtils tapPerformScanButton];
  [[self gscxtest_selectRingViewAtIndex:2]
      assertWithMatcher:
