#pragma mark - Private

- (void)gscxtest_tapShareReportButton {
  [[EarlGrey
      selectElementWithMatcher:grey_accessibilityID(kGSCXShareReportButtonAccessibilityIdentifier)]
      performAction:grey_tap()];
}

- (void)gscxtest_tapCancelShareReportButton {
  [[EarlGrey selectElementWithMatcher:[self gscxtest_matcherForCancelShareReportButton]]
      performAction:grey_tap()];
}

/**
 * @return A @c GREYMatcher instance that matches the button closing an activity view controller.
 */
- (id<GREYMatcher>)gscxtest_matcherForCancelShareReportButton {
  return grey_text(kGSCXTestSharingDelegateAlertDismissTitle);
}

- (GREYElementInteraction *)gscxtest_selectRingViewAtIndex:(NSInteger)index {
  NSString *ringViewAXId =
      [GSCXScannerScreenshotViewController accessibilityIdentifierForRingViewAtIndex:index];
  return [EarlGrey selectElementWithMatcher:grey_accessibilityID(ringViewAXId)];
}

/**
 * Swipes the gallery detail scroll view to the previous page.
 */
- (void)gscxtest_swipeToPreviousGalleryPage {
  [[EarlGrey
      selectElementWithMatcher:grey_accessibilityID(
                                   kGSCXContinuousScannerGalleryDetailViewAccessibilityIdentifier)]
      performAction:grey_swipeFastInDirection(kGREYDirectionRight)];
}

/**
 * Swipes the gallery detail scroll view to the next page.
 */
- (void)gscxtest_swipeToNextGalleryPage {
  [[EarlGrey
      selectElementWithMatcher:grey_accessibilityID(
                                   kGSCXContinuousScannerGalleryDetailViewAccessibilityIdentifier)]
      performAction:grey_swipeFastInDirection(kGREYDirectionLeft)];
}

- (void)gscxtest_tapBackButtonWithAccessibilityLabel:(NSString *)accessibilityLabel {
  id<GREYMatcher> matcher = grey_allOf(
      grey_ancestor(grey_keyWindow()), grey_ancestor(grey_kindOfClass([UINavigationBar class])),
      grey_ancestor(grey_accessibilityTrait(UIAccessibilityTraitButton)),
      grey_text(accessibilityLabel), nil);
  [[EarlGrey selectElementWithMatcher:matcher] performAction:grey_tap()];
}

- (void)gscxtest_assertRingViewCount:(NSInteger)count {
  for (NSInteger i = 0; i < count - 1; i++) {
    [[self gscxtest_selectRingViewAtIndex:i] assertWithMatcher:grey_notNil()];
  }
  [[self gscxtest_selectRingViewAtIndex:count] assertWithMatcher:grey_nil()];
}

/**
 * @return @c YES if the operating system is at iOS 13.0 or later, @c NO otherwise.
 */
- (BOOL)gscxtest_isAtLeastVersionIOS13 {
  if (@available(iOS 13, *)) {
    return YES;
  } else {
    return NO;
  }
}

/**
 * Asserts that a label with text equal to @c checkName is either sufficiently visible or not
 * sufficiently visible.
 *
 * @param checkName The text of the label to search for.
 * @param isSufficientlyVisible @c YES to assert the element is sufficiently visible, @c NO to
 * assert the element is not sufficiently visible.
 */
- (void)gscxtest_assertLabelForCheckNamed:(NSString *)checkName
                    isSufficientlyVisible:(BOOL)isSufficientlyVisible {
  id<GREYMatcher> matcher =
      isSufficientlyVisible ? grey_sufficientlyVisible() : grey_not(grey_sufficientlyVisible());
  [[EarlGrey selectElementWithMatcher:grey_text(checkName)] assertWithMatcher:matcher];
}

